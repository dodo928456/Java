import java.awt.Graphics;
import java.awt.Image;
import java.awt.Rectangle;
public class Sprite {
	protected int x;//현제 좌표x
	protected int y;//현제 좌표y
	protected int dx;//단위 시간에 움직이는 x방향 거리 
	protected int dy;//단위시간에 움직이는 y방향 거리
	protected Image image;//소프트라이트 가지고 있는 이미지
	
	//생성자
	public Sprite(Image image, int x, int y){
		this.image = image;
		this.x = x;
		this.y = y;
	}
	//소프라이트의 가로 기능반환
	public int getWidth(){
		return image.getWidth(null);
	}
	//세로 길이 반환
	public int getHeight() {
		return image.getHeight(null);
	}
	//스프라이트를 화면에 그린다/
	public void draw(Graphics g){
		g.drawImage(image, x, y, null);
	}
	//소프트라이트 움직인다
	public void move(){
		x+= dx;
		y+= dy;
	}
	//dx를 설정
	public void setDx(int dx){
		this.dx = dx;
	}
	//dy 설정
	public void setDy(int dy){
		this.dy = dy;
	}
	//dx를 반환
	public int getDx(){
		return dx;
	}
	//dy로 반환
	public int getDy(){
		return dy;
	}
	//xq반환
	public int getX(){
		return x;
	}
	//y반환
	public int getY(){
		return y;
	}
	
	public boolean checkCollision(Sprite other){
			Rectangle myRect = new Rectangle();
			Rectangle otherRect = new Rectangle();
			myRect.setBounds(x, y, getWidth(), getHeight());
			otherRect.setBounds(other.getX(), other.getY(), other.getWidth(), other.getHeight());
		
			return myRect.intersects(otherRect);
	}
	public void handleCollision(Sprite other){
		
	}

}
